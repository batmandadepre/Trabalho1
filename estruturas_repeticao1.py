# variavel declarada com tipo string
entrada_idade = ''
# looping com while e utilização do str devido a string atribuida como valor da variavel
while str(entrada_idade) != '0':
# utilização do imput para interação com o úsuario e entrada de dados
    entrada_idade = input ('Digite um número qualquer ou 0 para sair.')
    print ('Número digitado:', entrada_idade)
# print imprime o texto para interação com o úsuario, seguindo a impressão com a informação de dados armazenada digitada pelo úsuario, até que o número digitado 
# o número digitado seja o número que encerra a interação com o úsuario. #