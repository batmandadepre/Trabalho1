# variavel em texto declarada
texto = 'Olá, laço for.'
# condição de repetição com for para verificar os itens adicionados por caractere

for item in texto:
    print ('Caractere:', item)
# Laço for que itera sobre um intervalo numérico de 1 a 10
for numero in range (1, 11):
    print ('Número do intervalo:', str(numero))
#print imprime o texto em cada caractere assim como o número listando cada um